package com.emoticon.controllers;

import java.io.Serializable;
	
/** Class Notification that is passed to a timer in order to distinguish between the events
	 * @author Francesco Pongetti
	 * @author Sara Magliacane
	 */
public class TimerInfo implements Serializable{

	private static final long serialVersionUID = -3368574663065707554L;
	public int userid;
	public String info;
	
	public TimerInfo(int userid, String info) {
		super();
		this.userid = userid;
		this.info = info;
	}
	
}
